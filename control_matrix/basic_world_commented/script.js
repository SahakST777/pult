function setup() {
    frameRate(3);
    createCanvas(matrix[0].length * side, matrix.length * side); //կտավի չափերը դնել մատրիցայի չափերին համապատասխան
    background('#acacac');

    //Կրկնակի ցիկլը լցնում է օբյեկտներով խոտերի և խոտակերների զանգվածները
    //հիմնվելով մատրիցի վրա 
    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 6) {
                var ru = new run(x, y);
                runArr.push(ru);

            } 

        }
    }
}
function keyTyped() {
        if (key === 'w') {
                for (var i in runArr) {
                runArr[i].move(0);
            }
        }
         else if (key === 'a') {
                for (var i in runArr) {
                runArr[i].move(1);
            }
        }
          else if (key === 'd') {
                for (var i in runArr) {
               runArr[i].move(2);
            }
        }
          else if (key === 's') {
                for (var i in runArr) {
                runArr[i].move(3);
            }
        }
    }

function draw() {
    //Գծում է աշխարհը, հիմվելով matrix-ի վրա
    background('#acacac');
    for (var i = 0; i < matrix.length; i++) {
        for (var j = 0; j < matrix[i].length; j++) {
           
            if (matrix[i][j] == 6) {
                fill("orange");
                rect(j * side, i * side, side, side);
            } 
            else if (matrix[i][j] == 0) {
                fill('#acacac');
                rect(j * side, i * side, side, side);
            }
        }
    }
   keyTyped();

}















